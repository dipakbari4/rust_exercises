// This program demonstrates the symbol(s) of arithmetic operator
fn main() {
  let mut number = 12;

  println!("original {}", number);

  number = number + 3;
  println!("add {}", number);

  number = number - 5;
  println!("subtract {}", number);

  number = number / 2;
  println!("division {}", number);
  
  number = number * 6;
  println!("multiplication {}", number);

}